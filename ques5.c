#include<stdio.h>

int main() {

    int num = 3;

    //example of if-else statement.
    if (num < 5){
        printf("The number is less than five. \n");
    }
    else {
        printf("The number is greater than 5. \n");
    }
    
    //example of if-else statement.
    if (num < 5){
        printf("The number is less than five. \n");
    }
    else if(num = 5) {
        printf("The number is equal to 5.");
    }
    else {
        printf("The number is greater than 5. \n");
    }   
}